var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');


var app = express();


//引入art-template模板引擎
var template = require('art-template');
template.config('base', '');
template.config('extname', '.html');
app.engine('.html', template.__express);
app.set('view engine', 'html');
app.set('views', __dirname + '/views');

// uncomment after placing your favicon in /public
//app.use(favicon(path.join(__dirname, 'public', 'favicon.ico')));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));


//引入文本编辑器上传文件的模块
app.use('/common/kindeditor', require('./controllers/common/kindeditor/index'));

//导入子模块
app.use('/users', require('./controllers/users'));
app.use('/api/v1/blog_type', require('./controllers/blog/blog_type'));
app.use('/api/v1/blog', require('./controllers/blog/blog'));


//网站首页，重定向到博客类型展示
app.get('/blog_type', function (req, res) {
  res.redirect('/api/v1/blog_type/');
});
app.get('/blog', function (req, res) {
  res.redirect('/api/v1/blog/')
})






// catch 404 and forward to error handler
app.use(function(req, res, next) {
  var err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
